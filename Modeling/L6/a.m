function y= s(x,m)
%[url]http://wiki.web.ru/wiki/Метод_Скользящего_окна[/url]
lenx=size(x,2);
imin=1+(m-1)/2;
imax=lenx-(m-1)/2;
iset=imin:imax;
leni=length(iset);
y=zeros(size(x,1),leni);
for ik=1:leni
  ind=(iset(ik)-(m-1)/2):(iset(ik)+(m-1)/2);
  y(:,ik)=sum(x(:,ind),2)/m;
end
end
